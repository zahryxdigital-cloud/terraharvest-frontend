function BenefitCard({
  benefit,
  index,
}: {
  benefit: (typeof benefits)[0];
  index: number;
}) {
  const ref = useRef<HTMLDivElement>(null);
  const [visible, setVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setVisible(true);
          observer.disconnect();
        }
      },
      { threshold: 0.2 }
    );
    if (ref.current) observer.observe(ref.current);
    return () => observer.disconnect();
  }, []);

  return (
    <div
      ref={ref}
      className={`group relative p-7 rounded-2xl bg-cream border border-warm-beige/50 transition-all duration-500 hover:shadow-xl hover:shadow-muted-brown/8 hover:-translate-y-1 grain-overlay ${
        visible ? "animate-fade-in-up" : "opacity-0"
      }`}
      style={{ animationDelay: `${index * 100}ms` }}
    >
      {/* Icon */}
      <div className="relative z-10 w-14 h-14 rounded-xl bg-parchment border border-warm-beige/60 flex items-center justify-center text-burnt-orange mb-5 transition-all duration-300 group-hover:bg-burnt-orange group-hover:text-cream group-hover:border-burnt-orange group-hover:shadow-lg group-hover:shadow-burnt-orange/20">
        {benefit.icon}
      </div>

      {/* Content */}
      <div className="relative z-10">
        <h3 className="font-serif text-lg font-semibold text-dark-brown mb-2">
          {benefit.title}
        </h3>
        <p className="text-sm text-muted-brown leading-relaxed">
          {benefit.description}
        </p>
      </div>
    </div>
  );
}

